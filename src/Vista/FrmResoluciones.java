    /**
     * Función que convierte Fecha acadena de Caracteres
     *
     * @param dateParameter El parámetro contiene la fecha en formato Date a
     * Convertir en Cadena String
     * @return El valor que devuelve - Fecha en Formato Cadena -yyyy-MM-dd
     */
    public String fechaAString(Date dateParameter) {

        String unafecha = null;
        Date fecha = dateParameter;
        long d = fecha.getTime();
        java.sql.Date dateFinal = new java.sql.Date(d);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            unafecha = sdf.format(dateFinal);
        } catch (Exception e) {
            System.out.println("e = " + e.getMessage());
        }
        return unafecha;
    }

