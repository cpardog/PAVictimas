    /**
     * Función que convierte Cadena de Caracteres a Fecha SQL - Base de datos
     *
     * @param cadena La cadena que contine la fecha en formato Date a Convertir
     * a Date en SQL
     * @return El valor que devuelve - Date en SQL EN el Formato Indicado
     * -yyyy-MM-dd
     */
    public java.sql.Date cadenaAfecha(String cadena) {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate ff = LocalDate.parse(cadena, dtf);
        java.sql.Date res = java.sql.Date.valueOf(ff);
        return res;
    }

